<h1>Best Statistical Software Package</h1>

<p>The best statistical software package is the tool that works best for the user based upon what needs to be done, cost, and what works best with existing software. Similar to using a particular tool, each package has its advantages and disadvantages over each other. The major packages are R, SPSS, and SAS.</p>
<h2>R</h2>
<p><strong>R</strong> is a free open source statistical software language that is best used for writing custom statistical programs. It has the steepest learning curve but once you learn the language it is the most customizable and powerful out of the major packages. R has a very large community of statisticians who have written custom add-on packages.</p>
<h3>Advantages</h3>
<ul>
	<li>Free</li>
	<li>Fully customizable programming language</li>
	<li>Powerful graphics</li>
	<li>Interacts well with databases such as MYSQL</li>
	<li>Can interact with C++ to speed up processes</li>
	<li>Runs on Windows, Linux, and Macintosh operating systems</li>
</ul>
	
<h3>Disadvantages</h3>	
<ul>
	<li>Steepest learning curve</li>
</ul>

<h2>SPSS</h2>	

<p>The Statistical Package for the Social Sciences (SPSS) is a GUI based statistical software package that is popular in the social sciences. Because it is GUI based, it is good for running quick analyses. SPSS was also recently acquired by IBM which should help the software in the long run.</p>
<h3>Advantages</h3>
<ul>
	<li>GUI based program</li>
	<li>Quick descriptive statistics capability</li>
	<li>Most popular package in the social sciences</li>
	<li>Good for cluster analysis</li>
	<li>Runs on Windows, Linux, and Macintosh operating systems</li>
</ul>

<h3>Disadvantages</h3>
<ul>
	<li>Requires annual license</li>
	<li>Limited statistical procedures vs R or SAS</li>
	<li>Some procedures require purchase of add-on modules</li>
</ul>	

<h2>SAS</h2>
<p><strong>SAS</strong> (Statistical Analysis System) is the software used in the statistical analysis of clinical pharmaceutical trials for submission to the FDA. SAS has been in existence since 1976 and was originally developed for use on mainframe computers. Eventually as personal computers became popular and faster, SAS was developed for PCs. SAS is very popular in experimental design and ANOVA.</p>

<p>SAS is also a programming language and allows users to write macros which are custom data steps and procedures.</p>
<h3>Advantages</h3>
<ul>
	<li>Standard package of pharmaceutical industry</li>
	<li>Programming language is flexible although not as flexible/powerful as R</li>
	<li>Good for experimental design and ANOVA</li>
</ul>

<h3>Disadvantages</h3>
<ul>
	<li>Poor graphics capabilities although they have improved</li>
	<li>Requires annual license</li>
	<li>Limited statistical procedures vs R although more procedures than SPSS</li>
	<li>Some procedures require purchase of add-on components</li>
</ul>

	

